def detect_waf_cdn(headers: dict[str, str]) -> tuple[str | None, float]:
    flattened = " ".join([f"{key}:{value}".lower() for key, value in headers.items()])
    for provider, tokens in WAF_HEADER_TOKENS.items():
        matched = [token for token in tokens if token in flattened]
        if matched:
            return provider, min(0.98, 0.55 + (0.1 * len(matched)))
    return None, 0.0


def normalized_body_fingerprint(html_body: str) -> str:
    lowered = _normalize_body_tokens(html_body)
    return lowered[:400]


def detect_soft_404(
    primary_status: int | None,
    primary_body: str,
    random_status: int | None,
    random_body: str,
) -> bool:
    if random_status == 404:
        return False
    if primary_status is None or random_status is None:
        return False
    if random_status >= 500:
        return False
    primary_fp = normalized_body_fingerprint(primary_body)
    random_fp = normalized_body_fingerprint(random_body)
    if not primary_fp or not random_fp:
        return False
    if primary_fp == random_fp:
        return True
    overlap = len(set(primary_fp.split()) & set(random_fp.split()))
    return overlap >= 12 and abs(len(primary_fp) - len(random_fp)) <= 80


def favicon_hash(icon_bytes: bytes) -> str | None:
    if not icon_bytes:
        return None
    return hashlib.sha1(icon_bytes).hexdigest()  # noqa: S324


def extract_script_urls(base_url: str, html_body: str, same_host_only: bool = True) -> list[str]:
    scripts: list[str] = []
    base_host = (urlparse(base_url).hostname or "").lower()
    for match in SCRIPT_SRC_RE.finditer(html_body or ""):
        raw = (match.group(1) or "").strip()
        if not raw:
            continue
        candidate = urljoin(base_url, raw)
        parsed = urlparse(candidate)
        if parsed.scheme not in {"http", "https"}:
            continue
        host = (parsed.hostname or "").lower()
        if same_host_only and base_host and host and host != base_host:
            continue
        if candidate not in scripts:
            scripts.append(candidate)
    return scripts


def detect_redirect_loop(redirect_chain: list[dict[str, object]], final_url: str | None = None) -> tuple[bool, list[str]]:
    if not redirect_chain:
        return False, []
    seen: set[str] = set()
    reasons: list[str] = []
    for item in redirect_chain:
        source = str(item.get("from") or "")
        destination = str(item.get("to") or "")
        if destination in seen:
            reasons.append(f"redirect repeated destination {destination}")
            break
        if source == destination and source:
            reasons.append(f"self-redirect detected for {source}")
            break
        seen.add(source)
        seen.add(destination)
    if final_url and final_url in {str(item.get("from") or "") for item in redirect_chain}:
        reasons.append("final URL was previously seen in redirect chain")
    if len(redirect_chain) >= 4:
        reasons.append("long redirect chain may mask the real application")
    return bool(reasons), reasons[:4]


def detect_generic_http_response(
    primary_status: int | None,
    primary_body: str,
    primary_title: str | None,
    canary_responses: list[dict[str, object]],
) -> dict[str, object] | None:
    if primary_status not in {200, 403}:
        return None
    primary_fp = normalized_body_fingerprint(primary_body)
    if not primary_fp:
        return None
    matched: list[dict[str, object]] = []
    for canary in canary_responses:
        canary_status = canary.get("status_code")
        canary_body = str(canary.get("body_text") or "")
        if canary_status != primary_status:
            continue
        canary_fp = normalized_body_fingerprint(canary_body)
        if not canary_fp:
            continue
        if primary_fp == canary_fp:
            matched.append(canary)
            continue
        overlap = len(set(primary_fp.split()) & set(canary_fp.split()))
        if overlap >= 10 and abs(len(primary_fp) - len(canary_fp)) <= 120:
            matched.append(canary)
    if not matched:
        return None
    lowered = " ".join([primary_fp, (primary_title or "").lower()])
    reasons = [f"{len(matched)} canary responses closely matched the primary response"]
    if primary_status == 403 and any(token in lowered for token in GENERIC_403_TOKENS):
        reasons.append("403 response contains generic block-page markers")
    if primary_status == 200 and any(token in lowered for token in GENERIC_200_TOKENS):
        reasons.append("200 response looks like a generic placeholder or parking page")
    confidence = min(0.97, 0.6 + (0.12 * len(matched)))
    trust_score = max(0.15, 0.55 - (0.12 * len(matched)))
    return {
        "status_code": primary_status,
        "trust_score": round(trust_score, 2),
        "confidence": round(confidence, 2),
        "reasons": reasons,
        "matched_canaries": len(matched),
    }


def detect_cdn_parking_page(
    headers: dict[str, str],
    title: str | None,
    body_text: str,
    page_url: str,
) -> tuple[str | None, list[str]]:
    combined = " ".join(
        [
            " ".join(f"{key}:{value}".lower() for key, value in headers.items()),
            (title or "").lower(),
            _normalize_body_tokens(body_text)[:600],
            page_url.lower(),
        ]
    )
    for provider, tokens in PARKING_PAGE_TOKENS.items():
        reasons = [token for token in tokens if token in combined]
        if reasons:
            return provider, reasons[:4]
    return None, []


def assess_response_trust(
    generic_response: dict[str, object] | None,
    redirect_loop: bool,
    parking_provider: str | None,
    waf_provider: str | None,
) -> tuple[float, list[str]]:
    trust = 0.9
    reasons: list[str] = []
    if generic_response:
        trust = min(trust, float(generic_response.get("trust_score", 0.4)))
        reasons.extend(str(item) for item in generic_response.get("reasons", []))
    if redirect_loop:
        trust = min(trust, 0.45)
        reasons.append("redirect behavior may be hiding the true application response")
    if parking_provider:
        trust = min(trust, 0.35)
        reasons.append(f"{parking_provider} parking/default response detected")
    if waf_provider and not generic_response:
        trust = min(trust, 0.65)
        reasons.append(f"{waf_provider} may be filtering or normalizing responses")
    return round(trust, 2), reasons[:6]


def explain_technology_fingerprints(headers: dict[str, str], html_body: str) -> list[dict[str, object]]:
    lowered_body = html_body.lower()
    rows: list[dict[str, object]] = []
    for name, version, confidence in detect_technologies(headers, html_body):
        lowered_name = name.lower()
        sources: list[str] = []
        version_sources: list[str] = []
        if lowered_name in {"web server", "nginx", "apache http server", "microsoft iis", "cloudflare"}:
            if headers.get("server"):
                sources.append("header")
                if version and VERSION_RE.search(headers.get("server", "")):
                    version_sources.append("header")
        elif lowered_name in {"x-powered-by", "php", "asp.net", "express"}:
            if headers.get("x-powered-by"):
                sources.append("header")
                if version and VERSION_RE.search(headers.get("x-powered-by", "")):
                    version_sources.append("header")
        elif lowered_name == "wordpress":
            if "wp-content" in lowered_body or "wordpress" in lowered_body:
                sources.append("body")
            if "x-generator" in headers and "wordpress" in headers.get("x-generator", "").lower():
                sources.append("header")
                if version:
                    version_sources.append("header")
            if version and "body" in sources:
                version_sources.append("body")
        else:
            if lowered_name and lowered_name.replace(".", "") in lowered_body.replace(".", ""):
                sources.append("body")
            if version:
                version_sources.append("body")
        unique_sources = sorted(set(sources)) or ["body"]
        unique_version_sources = sorted(set(version_sources))
        if len(unique_version_sources) >= 2:
            tier = "multi-source corroborated"
        elif any(name.startswith(prefix) for prefix in APPLIANCE_MARKERS):
            tier = "strong appliance fingerprint"
        elif unique_sources == ["header"] and version:
            tier = "header only"
        elif version and {"header", "body"} <= set(unique_sources):
            tier = "banner + body"
        elif version and unique_sources:
            tier = "banner + body" if "body" in unique_sources else "header only"
        else:
            tier = "body only"
        rows.append(
            {
                "name": name,
                "version": version,
                "confidence": round(float(confidence), 2),
                "sources": unique_sources,
                "version_confidence_tier": tier,
            }
        )
    return rows


def detect_cloud_storage_posture(
    page_url: str,
    headers: dict[str, str],
    body_text: str,
) -> dict[str, object] | None:
    parsed = urlparse(page_url)
    host = (parsed.hostname or "").lower()
    provider = None
    for suffix, provider_name in STORAGE_SUFFIXES.items():
        if host == suffix or host.endswith(f".{suffix}"):
            provider = provider_name
            break
    lowered_headers = " ".join(f"{key}:{value}".lower() for key, value in headers.items())
    lowered_body = _normalize_body_tokens(body_text)[:1200]
    if provider is None:
        if "x-amz-bucket-region" in lowered_headers or "x-amz-request-id" in lowered_headers:
            provider = "aws"
        elif "x-goog-storage-class" in lowered_headers or "googleapis" in lowered_headers:
            provider = "gcp"
        elif "x-ms-request-id" in lowered_headers or "x-ms-version" in lowered_headers:
            provider = "azure"
    if provider is None:
        return None
    query = parse_qs(parsed.query)
    object_listing = any(
        marker in lowered_body
        for marker in ("listbucketresult", "<contents>", "<blob>", "<enumerationresults>", "<key>")
    )
    static_site = any(
        marker in lowered_body or marker in lowered_headers
        for marker in ("static website hosting", "amazon s3", "x-amz-website", "azure static web apps")
    )
    signed_url = any(
        key.lower() in {"x-amz-signature", "x-amz-credential", "googleaccessid", "signature", "expires", "sv", "sp", "sig", "se"}
        for key in query
    )
    bucket_name = host.split(".", 1)[0] if "." in host else host
    source_host = ""
    if parsed.netloc:
        source_host = parsed.netloc.split(":")[0].lower()
    naming_reuse = bool(bucket_name and source_host and bucket_name in source_host)
    reasons = [f"{provider} storage-like endpoint detected"]
    if object_listing:
        reasons.append("object listing markers detected")
    if static_site:
        reasons.append("static hosting markers detected")
    if signed_url:
        reasons.append("signed URL markers detected")
    if naming_reuse:
        reasons.append("bucket/container name appears tied to target naming")
    return {
        "provider": provider,
        "object_listing": object_listing,
        "static_site": static_site,
        "signed_url": signed_url,
        "bucket_name": bucket_name,
        "naming_reuse": naming_reuse,
        "reasons": reasons,
    }


def extract_structured_urls(base_url: str, body_text: str, same_host_only: bool = True) -> list[str]:
    structured: list[str] = []
    base_host = (urlparse(base_url).hostname or "").lower()
    candidates = []
    candidates.extend(XML_LOC_RE.findall(body_text or ""))
    candidates.extend(re.findall(r"(?im)^(?:allow|disallow|sitemap):\s*(\S+)$", body_text or ""))
    for candidate in candidates:
        absolute = urljoin(base_url, candidate.strip())
        parsed = urlparse(absolute)
        if parsed.scheme not in {"http", "https"}:
            continue
        host = (parsed.hostname or "").lower()
        if same_host_only and base_host and host and host != base_host:
            continue
        if absolute not in structured:
            structured.append(absolute)
    return structured
