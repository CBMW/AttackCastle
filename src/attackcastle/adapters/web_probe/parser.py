TITLE_RE = re.compile(r"<title[^>]*>(.*?)</title>", re.IGNORECASE | re.DOTALL)
FORM_RE = re.compile(r"<form\b", re.IGNORECASE)
INPUT_NAME_RE = re.compile(r"<input[^>]*\bname=['\"]?([A-Za-z0-9_\-:\[\]\.]+)", re.IGNORECASE)
WP_VERSION_RE = re.compile(r"wp-(?:content|includes).{0,80}?ver=([0-9][0-9A-Za-z\.\-_]+)", re.IGNORECASE)
PASSWORD_RE = re.compile(r"type=['\"]?password['\"]?", re.IGNORECASE)
SCRIPT_SRC_RE = re.compile(r"""<script[^>]*\bsrc=['"]([^'"]+)['"]""", re.IGNORECASE)
VERSION_RE = re.compile(r"([0-9]+(?:\.[0-9A-Za-z\-_]+){1,4})")
XML_LOC_RE = re.compile(r"<loc>\s*([^<]+)\s*</loc>", re.IGNORECASE)

