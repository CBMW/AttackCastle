def extract_title(html_body: str) -> str | None:
    match = TITLE_RE.search(html_body)
    if not match:
        return None
    return unescape(match.group(1)).strip()[:300]


def count_forms(html_body: str) -> int:
    return len(FORM_RE.findall(html_body))


def extract_input_parameters(html_body: str, page_url: str) -> list[str]:
    names = {item.strip() for item in INPUT_NAME_RE.findall(html_body) if item and item.strip()}
    parsed = urlparse(page_url)
    for key in parse_qs(parsed.query).keys():
        if key.strip():
            names.add(key.strip())
    return sorted(names)


def detect_wordpress(html_body: str, headers: dict[str, str]) -> tuple[bool, str | None]:
    lowered = html_body.lower()
    if "wp-content" not in lowered and "wordpress" not in lowered:
        return False, None
    version_match = WP_VERSION_RE.search(html_body)
    version = version_match.group(1) if version_match else None
    if not version:
        generator = headers.get("x-generator", "")
        if "wordpress" in generator.lower():
            parts = generator.split()
            if len(parts) > 1:
                version = parts[-1]
    return True, version


def detect_technologies(headers: dict[str, str], html_body: str) -> list[tuple[str, str | None, float]]:
    technologies: list[tuple[str, str | None, float]] = []
    server = headers.get("server")
    if server:
        technologies.append(("Web Server", server, 0.7))
        lowered = server.lower()
        if "nginx" in lowered:
            technologies.append(("Nginx", server, 0.9))
        if "apache" in lowered:
            technologies.append(("Apache HTTP Server", server, 0.9))
        if "iis" in lowered:
            technologies.append(("Microsoft IIS", server, 0.9))

    powered_by = headers.get("x-powered-by")
    if powered_by:
        technologies.append(("X-Powered-By", powered_by, 0.7))
        lowered = powered_by.lower()
        if "php" in lowered:
            technologies.append(("PHP", powered_by, 0.9))
        if "asp.net" in lowered:
            technologies.append(("ASP.NET", powered_by, 0.9))
        if "express" in lowered:
            technologies.append(("Express", powered_by, 0.8))

    if "cloudflare" in headers.get("server", "").lower():
        technologies.append(("Cloudflare", None, 0.85))

    wp_detected, wp_version = detect_wordpress(html_body, headers)
    if wp_detected:
        technologies.append(("WordPress", wp_version, 0.95))

    lowered_body = html_body.lower()
    if "drupal-settings-json" in lowered_body or "sites/default/files" in lowered_body:
        technologies.append(("Drupal", None, 0.9))
    if "joomla!" in lowered_body or "/media/system/js/" in lowered_body:
        technologies.append(("Joomla", None, 0.85))
    if "_next/static" in lowered_body:
        technologies.append(("Next.js", None, 0.88))
    if "laravel" in lowered_body or "csrf-token" in lowered_body:
        technologies.append(("Laravel", None, 0.78))
    if "react" in lowered_body:
        technologies.append(("React", None, 0.65))
    for appliance_name, tokens in APPLIANCE_MARKERS.items():
        if any(token in lowered_body for token in tokens):
            version = _extract_nearby_version(lowered_body, tokens)
            technologies.append((appliance_name, version, 0.93 if version else 0.88))

    return technologies


def _extract_nearby_version(text: str, tokens: tuple[str, ...]) -> str | None:
    for token in tokens:
        index = text.find(token)
        if index < 0:
            continue
        window = text[max(0, index - 60) : index + 120]
        match = VERSION_RE.search(window)
        if match:
            return match.group(1)
    return None


def _normalize_body_tokens(value: str) -> str:
    return re.sub(r"\s+", " ", re.sub(r"[0-9a-f]{8,}", "", value.lower())).strip()


def detect_login_portal(
    html_body: str,
    headers: dict[str, str],
    page_url: str,
    title: str | None,
    input_parameters: list[str],
) -> tuple[bool, list[str]]:
    lowered = " ".join(
        [
            html_body.lower(),
            (title or "").lower(),
            page_url.lower(),
            " ".join(item.lower() for item in input_parameters),
            headers.get("www-authenticate", "").lower(),
        ]
    )
    reasons: list[str] = []
    if PASSWORD_RE.search(html_body):
        reasons.append("password input field detected")
    if any(token in lowered for token in LOGIN_TOKENS):
        reasons.append("login-oriented terms detected")
    if {"username", "email", "user", "login"} & {item.lower() for item in input_parameters}:
        reasons.append("credential-style input names detected")
    return bool(reasons), reasons


def detect_admin_interface(html_body: str, page_url: str, title: str | None) -> tuple[bool, list[str]]:
    lowered = " ".join([html_body.lower(), page_url.lower(), (title or "").lower()])
